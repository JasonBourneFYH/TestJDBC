package cn.icss.stu_sourse;

public class Student {
	private String stu_id;
	private String stu_name;
	private String stu_pass;
	private String th_id;

	public String getStu_id() {
		return stu_id;
	}

	public void setStu_id(String stu_id) {
		this.stu_id = stu_id;
	}

	public String getStu_name() {
		return stu_name;
	}

	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}

	public String getStu_pass() {
		return stu_pass;
	}

	public void setStu_pass(String stu_pass) {
		this.stu_pass = stu_pass;
	}

	public String getTh_id() {
		return th_id;
	}

	public void setTh_id(String th_id) {
		this.th_id = th_id;
	}

}
